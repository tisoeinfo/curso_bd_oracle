-- CREACION DE VISTAS EN SQL
CREATE VIEW VIEW_REPORTE_CLIENTES_ATENDIDOS AS
SELECT 
R.ID_RESERVA,
R.FECHA,
C.NOMBRE AS 'NOMBRE_CLIENTE',
CONCAT(M.NOMBRE, ' ',M.APE_PATERNO, ' ', M.APE_MATERNO) AS 'NOMBRE_MESERO'
FROM RESERVAS R
INNER JOIN CLIENTES C ON C.ID_CLIENTE=R.ID_CLIENTE
INNER JOIN MESEROS M ON M.ID_MESERO=R.ID_MESERO;

SELECT  * FROM VIEW_REPORTE_CLIENTES_ATENDIDOS;
