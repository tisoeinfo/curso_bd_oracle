-- CREANDO EL OBJETO (EN POO SE LE LLAMA CLASE) 
CREATE OR REPLACE TYPE PERSONA AS OBJECT (
   NOMBRE VARCHAR2(50)
   MEMBER PROCEDURE MOSTRAR 
);

-- CREANDO EL BODY Y LLLAMANDO AL PROCEDIMIENTO
CREATE OR REPLACE TYPE BODY PERSONA AS 
   MEMBER PROCEDURE MOSTRAR IS
     BEGIN
       dbms_output.put_line('Nombre: ' || nombre );
    END;
END;

-- ELIMINAR OBJETO BODY
DROP TYPE BODY PERSONA;


-- INSTANCIAR (CREANDO EL OBJETO)
DECLARE 
p PERSONA 
BEGIN
:= PERSONA('Luis');
     p.mostrar;
END;


